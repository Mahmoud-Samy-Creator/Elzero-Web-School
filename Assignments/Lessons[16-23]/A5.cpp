#include <iostream>
using namespace std;

int main()
{
    char a = '~';
    char b = '&';
    char c = '%';
    char d = 'A';

    cout << int(a) << endl;
    cout << int(b) << endl;
    cout << int(c) << endl;
    cout << int(d) << endl;
}


// Output Needed
/*
"ASCII Value of ~ Is 126"
"ASCII Value of & Is 38"
"ASCII Value of % Is 37"
"ASCII Value of A Is 65"
*/