#include "main.h"

int main()
{
    char a = '~';
    char b = '&';
    char c = '%';
    char d = 'A';

    cout << "ASCII Value of ~ " << int(a) << endl;
    cout << "ASCII Value of & " << int(b) << endl;
    cout << "ASCII Value of % " << int(c) << endl;
    cout << "ASCII Value of A " << int(d) << endl;
}


