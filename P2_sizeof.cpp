#include <iostream>
using namespace std;

int main()
{
    cout << "Size of integer datatype: " << sizeof(int) << endl;
    cout << "Size of double datatype: " << sizeof(double) << endl;
    cout << "Size of float datatype: " << sizeof(float) << endl;
    cout << "Size of character datatype: " << sizeof(char) << endl;
    cout << "Size of boolen datatype: " << sizeof(bool) << endl;

    return 0;
}